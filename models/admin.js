/**
 * ===========================================
 * Export model functions as a module
 * ===========================================
 */
module.exports = (dbPoolInstance) => {

  // `dbPoolInstance` is accessible within this function scope
  let getAll = (data,callback) => {

    console.log("model from",data)
    let query = `SELECT * FROM admin WHERE username='${data.username}' AND password='${data.password}'`;

    dbPoolInstance.query(query, (error, queryResult) => {
      if( error ){

        // invoke callback function with results after query has executed
        callback(error, null);

      }else{

        // invoke callback function with results after query has executed

        if( queryResult.rows.length > 0 ){
          callback(null, queryResult.rows);

        }else{
          callback(null, null);

        }
      }
    });
  };


    //CALLS UP LIST OF TASKS AND ORDERS THAT NEEDS TO BE FULFILLED
    let tasks = (callback) => {

    let query = `SELECT * FROM orders WHERE fulfilment ='pending' ORDER BY id DESC`;

    dbPoolInstance.query(query, (error, queryResult) => {
      if( error ){

        // invoke callback function with results after query has executed
        callback(error, null);

      }else{

        // invoke callback function with results after query has executed

        if( queryResult.rows.length > 0 ){
          callback(null, queryResult.rows);

        }else{
          callback(null, null);

        }
      }
    });
  };

      //CALLS UP EVERY SINGLE TASK IN THE DATABASE!
    let masterlist = (callback) => {

    let query = `SELECT * FROM orders ORDER BY id DESC`;

    dbPoolInstance.query(query, (error, queryResult) => {
      if( error ){

        // invoke callback function with results after query has executed
        callback(error, null);

      }else{

        // invoke callback function with results after query has executed

        if( queryResult.rows.length > 0 ){
          callback(null, queryResult.rows);

        }else{
          callback(null, null);

        }
      }
    });
  };

  return {
    getAll,
    tasks,
    masterlist
  };
};